let diaSemana = 'Miércoles';
switch (diaSemana) {
  case 'Lunes':
    console.log('Está iniciando la semana');
    console.log('Esto también ocurre el lunes');
    break;
  case 'Martes':
    console.log('Recordar llevar el nene al fútbol');
    break;
  case 'Sábado':
    console.log('Hoooooooo');
  case 'Domingo':
    console.log('Disfruta del día de la semana');
    break;
  default:
    console.log('Trabaja duro!!');
}